// Generate src/xterm-assets.ts by inlining xterm.js + its CSS + the fit addon as
// string constants. The terminal renders inside a react-native-webview, which is
// offline and CSP-restricted — it can't pull xterm from a CDN or read node_modules
// at runtime — so the library source must be embedded in the JS bundle.
//
// JSON.stringify (not a template literal) makes each blob a safe JS string literal
// regardless of backticks/${} inside the minified source.
//
// Regenerate after bumping @xterm/*:  node scripts/gen-xterm-assets.mjs
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const mobile = dirname(here);
const read = (p) => readFileSync(join(mobile, "node_modules", p), "utf8");

const xtermJs = read("@xterm/xterm/lib/xterm.js");
const xtermCss = read("@xterm/xterm/css/xterm.css");
const fitJs = read("@xterm/addon-fit/lib/addon-fit.js");

const out = `// GENERATED by scripts/gen-xterm-assets.mjs — do not edit by hand.
// xterm.js + CSS + the fit addon, inlined for the offline webview terminal.
/* biome-ignore-all lint: generated third-party bundle */
export const XTERM_JS = ${JSON.stringify(xtermJs)};
export const XTERM_CSS = ${JSON.stringify(xtermCss)};
export const FIT_ADDON_JS = ${JSON.stringify(fitJs)};
`;

writeFileSync(join(mobile, "src", "xterm-assets.ts"), out);
console.log(
	`wrote src/xterm-assets.ts (xterm ${(xtermJs.length / 1024) | 0}KB, css ${(xtermCss.length / 1024) | 0}KB, fit ${(fitJs.length / 1024) | 0}KB)`,
);
